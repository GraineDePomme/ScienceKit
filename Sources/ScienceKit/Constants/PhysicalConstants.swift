import Foundation

extension Double {

    /// The standard acceleration of gravitation : m . s^-2
    public static let standardGravityAcceleration: Double = 9.806_65

    /// The Newtonian constant of gravitation : m^3 kg^-1 s^-2
    public static let newtonianGravitationalConstant: Double = 6.674_30e-11

    /// The mass of the Earth : kg
    public static let earthMass: Double = 5.9736e24

    /// The volumetric mean radius of the Earth : m
    public static let earthRadius: Double = 6371e3

}